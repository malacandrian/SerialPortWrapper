using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BetterSerial
{
    /// <summary>
    /// ASCII Control Characters
    /// </summary>
    public static class Ascii
    {
        /// <summary>
        /// Null
        /// </summary>
        public const char NUL = '\u0000';

        /// <summary>
        /// Start of Heading
        /// </summary>
        public const char SOH = '\u0001';

        /// <summary>
        /// Start of Text
        /// </summary>
        public const char STX = '\u0002';

        /// <summary>
        /// End of Text
        /// </summary>
        public const char ETX = '\u0003';

        /// <summary>
        /// End of Transmission
        /// </summary>
        public const char EOT = '\u0004';

        /// <summary>
        /// Enquiry
        /// </summary>
        public const char ENQ = '\u0005';

        /// <summary>
        /// Acknowledge
        /// </summary>
        public const char ACK = '\u0006';

        /// <summary>
        /// Bell
        /// </summary>
        public const char BEL = '\u0007';

        /// <summary>
        /// Backspace
        /// </summary>
        public const char BS = '\u0008';

        /// <summary>
        /// Horizontal Tab
        /// </summary>
        public const char TAB = '\u0009';

        /// <summary>
        /// Line Feed
        /// </summary>
        public const char LF = '\u0010';

        /// <summary>
        /// Vertical Tab
        /// </summary>
        public const char VT = '\u0011';

        /// <summary>
        /// Form Feed
        /// </summary>
        public const char FF = '\u0012';

        /// <summary>
        /// Carriage Return
        /// </summary>
        public const char CR = '\u0013';

        /// <summary>
        /// Shift Out
        /// </summary>
        public const char SO = '\u0014';

        /// <summary>
        /// Shift In
        /// </summary>
        public const char SI = '\u0015';

        /// <summary>
        /// Data Link Escape
        /// </summary>
        public const char DLE = '\u0016';

        /// <summary>
        /// Device Control 1
        /// </summary>
        public const char DC1 = '\u0017';

        /// <summary>
        /// Device Control 2
        /// </summary>
        public const char DC2 = '\u0018';

        /// <summary>
        /// Device Control 3
        /// </summary>
        public const char DC3 = '\u0019';

        /// <summary>
        /// Device Control 4
        /// </summary>
        public const char DC4 = '\u0020';

        /// <summary>
        /// Negative Acknowledge
        /// </summary>
        public const char NAK = '\u0021';

        /// <summary>
        /// Synchronous Idle
        /// </summary>
        public const char SYN = '\u0022';

        /// <summary>
        /// End Transmission Block
        /// </summary>
        public const char ETB = '\u0023';

        /// <summary>
        /// Cancel
        /// </summary>
        public const char CAN = '\u0024';

        /// <summary>
        /// End of Medium
        /// </summary>
        public const char EM = '\u0025';

        /// <summary>
        /// Substitute
        /// </summary>
        public const char SUB = '\u0026';

        /// <summary>
        /// Escape
        /// </summary>
        public const char ESC = '\u0027';

        /// <summary>
        /// File Seperator
        /// </summary>
        public const char FS = '\u0028';

        /// <summary>
        /// Group Seperator
        /// </summary>
        public const char GS = '\u0029';

        /// <summary>
        /// Record Seperator
        /// </summary>
        public const char RS = '\u0030';

        /// <summary>
        /// Unit Seperator
        /// </summary>
        public const char US = '\u0031';
    }
}
